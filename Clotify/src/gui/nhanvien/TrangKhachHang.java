    void loadDataKhachHang(){
        model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
     
        for (KhachHang kh : khs.getKhachHang()) {
            model.addRow(new Object[]{
                kh.getIdKH(),
                kh.getMaKH(),
                kh.getTenKH(),
                kh.getGioiTinh(),
                kh.getSdt(),
                kh.getDiaChi()
            });
        }
    }
    void loadChiTietKH(int idKH){
         DefaultTableModel modelChiTietKhachHang = (DefaultTableModel) tblChiTietKH.getModel();
                     modelChiTietKhachHang.setRowCount(0);
                     for (KhachHang kh : khs.getChiTietKhachHang(idKH)) {
                         modelChiTietKhachHang.addRow(new Object[]{
                             kh.getMaHD(),
                             kh.getMaSP(),
                             kh.getTenSP(),
                             kh.getTenMS(),
                             kh.getTenCL(),
                             kh.getTenSize(),
                             kh.getTenTH(),
                             kh.getNgayTao(),
                             kh.getTongTien()
                         });
                     }
    }
    KhachHang getForm(){
        KhachHang kh = new KhachHang();
        kh.setTenKH(txtTenKH.getText());
        kh.setSdt(txtSDT.getText());
        kh.setDiaChi(txtDiaChi.getText());
        String gioiTinh ="";
        if(rdNam.isSelected()){
            gioiTinh = "Nam";
            }else{
            gioiTinh = "Nữ";
        }
        kh.setGioiTinh(gioiTinh);
        return kh;
    }
    void setForm(KhachHang kh){
        txtTenKH.setText(kh.getTenKH());
        txtSDT.setText(kh.getSdt());
        txtDiaChi.setText(kh.getDiaChi());
        if(kh.getGioiTinh().equals("Nam")){
            rdNam.setSelected(true);
        }else{
            rdNu.setSelected(true);
        }
        kh.getGioiTinh();
    }

