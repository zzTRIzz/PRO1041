package model;

/**
 *
 * @author Surface
 */
public class ThuongHieu {
    int idTH;
    String maTH, tenTH,ghiChu;

    public ThuongHieu() {
    }

    public ThuongHieu(int idTH, String maTH, String tenTH, String ghiChu) {
        this.idTH = idTH;
        this.maTH = maTH;
        this.tenTH = tenTH;
        this.ghiChu = ghiChu;
    }

    public int getIdTH() {
        return idTH;
    }

    public void setIdTH(int idTH) {
        this.idTH = idTH;
    }

    public String getMaTH() {
        return maTH;
    }

    public void setMaTH(String maTH) {
        this.maTH = maTH;
    }

    public String getTenTH() {
        return tenTH;
    }

    public void setTenTH(String tenTH) {
        this.tenTH = tenTH;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }
    
}
