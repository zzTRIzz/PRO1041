package Model;

/**
 *
 * @author ZznamnhizZ
 */
public class Size {
    Integer idSize;
    String maSize;
    String tenSize;
    String ghiChu;

    public Size() {
    }

    public Size(Integer idSize, String maSize, String tenSize, String ghiChu) {
        this.idSize = idSize;
        this.maSize = maSize;
        this.tenSize = tenSize;
        this.ghiChu = ghiChu;
    }

    public Integer getIdSize() {
        return idSize;
    }

    public void setIdSize(Integer idSize) {
        this.idSize = idSize;
    }

    public String getMaSize() {
        return maSize;
    }

    public void setMaSize(String maSize) {
        this.maSize = maSize;
    }

    public String getTenSize() {
        return tenSize;
    }

    public void setTenSize(String tenSize) {
        this.tenSize = tenSize;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }
    
    
}
